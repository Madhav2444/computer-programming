# Question 7: Write a Python program to find the maximum and minimum values in a dictionary.
# Input: {'a': 10, 'b': 20, 'c': 5}
# Output: Max: 20, Min: 5
dict = {'a': 10, 'b': 20, 'c': 5}
max_value = max(dict.values())
min_value = min(dict.values())
print("Max value:", max_value)
print("Min value:", min_value)
